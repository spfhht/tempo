package com.delivery.rocket.corp.managermanagement.dto;

public class ManagerManagementDto {
	private String manager_id;
	private String manager_name;
	private String c_id;
	private String manager_pwd;

	public String getManager_id() {
		return manager_id;
	}

	public void setManager_id(String manager_id) {
		this.manager_id = manager_id;
	}

	public String getManager_name() {
		return manager_name;
	}

	public void setManager_name(String manager_name) {
		this.manager_name = manager_name;
	}

	public String getC_id() {
		return c_id;
	}

	public void setC_id(String c_id) {
		this.c_id = c_id;
	}

	public String getManager_pwd() {
		return manager_pwd;
	}

	public void setManager_pwd(String manager_pwd) {
		this.manager_pwd = manager_pwd;
	}

}
